<script type="text/javascript">
    var name = 'Perfume PLC.';
    name += `\n Customer Receivable List`;
    var reportTittle = name;

    var dataTableOptions = {
        dom: '<"top"fB>rt<"bottom"lip>',
        buttons: [{
            extend: 'collection',
            text: 'Export',
            className: 'btn btn-sm btn-light-primary',

            buttons: [{
                    extend: 'excel',
                    text: 'Excel',
                    title: `${reportTittle}`,
                    className: 'btn btn-sm btn-light-primary'
                },
                {
                    extend: 'copy',
                    text: 'Copy',
                    title: `${reportTittle}`,
                    className: 'btn btn-sm btn-light-primary'
                },
                {
                    extend: 'csv',
                    text: 'CSV',
                    title: `${reportTittle}`,
                    className: 'btn btn-sm btn-light-primary'
                },
                {
                    extend: 'print',
                    text: 'Print',
                    title: `${reportTittle}`,
                    className: 'btn btn-sm btn-light-primary'
                }
            ]
        }],
        paging: true,
        ordering: false,
        searching: true,
        responsive: false,
        lengthMenu: [10, 25, 50, 100],
        pageLength: 25,
        language: {
            lengthMenu: 'Show _MENU_ entries',
            search: 'Search:',
            paginate: {
                first: 'First',
                last: 'Last',
                next: 'Next',
                previous: 'Previous'
            }
        }
    };

    var table = $('#customer_receivable_list').DataTable(dataTableOptions);

    function CallBack(pdfdata) {
        let url;
        url = '{{ route('total_customer_receivable_list_search', ['pdf' => ':pdf']) }}';
        url = url.replace(':pdf', pdfdata);

        console.log('url:', url);
        if (pdfdata == 'list') {
            $.ajax({
                url: url,
                type: "GET",
                data: {
                    "_token": "{{ csrf_token() }}"
                },
                dataType: "json",
                success: function(data) {
                    console.log('data:', data);
                    $('#PDFDownloadDiv').removeClass('d-none');

                    table.clear().draw();
                    if (data.length > 0) {
                        $('#jsdataerror').text('');
                        let id = 1;
                        $.each(data, function(key, value) {
                            let d = data[key];
                            let amount = parseFloat(d.due) || 0;
                            table.row.add([id, d.account_name, d.account_mobile, `<div class="text-end">${formatCurrency(amount)}</div>`
                            ]).draw();
                            id++;
                        });
                        table.buttons().enable();
                    } else {
                        $('#jsdataerror').text('No records found');
                        console.log('No records found');
                    }
                }
            });
        } else if (pdfdata == 'pdfurl') {
            $('#downloadPdf').attr('href', url);
        } else {
            $('#jsdataerror').text('Please select a date');
        }
    }
    CallBack("list");

    $('#downloadPdf').on('click', function() {
        CallBack("pdfurl");
    });
</script>
